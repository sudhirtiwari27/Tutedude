# Task 2: Using the Math Module for Calculations

import math
print("Enter a number: ", end="")
num=int(input())
print("Square root of ",num," is: ", math.sqrt(num))
print("Natural logarithm (log base e) of ",num," is: ", math.log(num))
print("Sine of the ",num," is (in radians): ", math.sin(num))
